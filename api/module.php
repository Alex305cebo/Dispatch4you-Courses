<?php
// api/module.php — отдаёт HTML платного модуля ТОЛЬКО после проверки доступа.
//
// Суть починки: раньше сервер отдавал модуль целиком кому угодно, а role-guard.js
// лишь накрывал уже присланный текст оверлеем — контент забирался через curl,
// Ctrl+U или удалением одного div. Теперь HTML физически не покидает сервер,
// пока подпись куки не подтвердит роль.
//
// Файлы модулей лежат в /protected/modules/ — там .htaccess «Require all denied»,
// то есть по HTTP их не достать. readfile() читает их с ДИСКА, минуя веб-сервер,
// поэтому запрет на них не распространяется.
//
// Подключается через .htaccess:
//   RewriteRule ^pages/(doc-module-\d+-complete)\.html$ /api/module.php?m=$1 [L]

require_once __DIR__ . '/d4y-session-lib.php';

// ── Включение и рубильник — это ОДИН И ТОТ ЖЕ файл ───────────────────────────
// Нет ~/domains/dispatch4you.com/session.key → гейт СПИТ: модули отдаются всем,
// ровно как до этой правки. Появился ключ → гейт работает.
//
// Отсюда два следствия, ради которых так и сделано:
//   ВКЛЮЧИТЬ  — создать session.key. Без деплоя, без git.
//   ОТКАТИТЬ  — переименовать/удалить session.key. Мгновенно, без деплоя.
//
// Почему «спит», а не «закрывает всё»: за этим гейтом сидят люди, которые уже
// заплатили. Ошибка в конфиге не должна их выкидывать — она должна вернуть
// поведение, которое и так работало месяцами. Дыра при этом не хуже прежней.
//
// ponytail: fail-open — осознанный компромисс на время выкатки. Когда гейт
// проверен на живом студенте, поведение стоит перевернуть на fail-closed
// (нет ключа → 503), иначе случайное удаление ключа тихо откроет платный
// контент и никто этого не заметит.

$page = isset($_GET['m']) ? (string) $_GET['m'] : '';
// Белый список по строгому шаблону: имя приходит из URL, а мы его подставляем
// в путь. Без этой проверки '../../' увёл бы readfile() куда угодно.
if (!preg_match('/^doc-module-([1-9]|1[0-2])-complete$/', $page)) {
  http_response_code(404);
  header('Content-Type: text/plain; charset=utf-8');
  echo 'not found';
  exit;
}

// Язык — только из двух значений, в путь идёт наш литерал, а не строка из URL.
$lang = (isset($_GET['lang']) && $_GET['lang'] === 'en') ? 'en' : 'ru';
$dir  = ($lang === 'en') ? 'en/' : '';

$file = __DIR__ . '/../protected/modules/' . $dir . $page . '.html';
if (!is_readable($file)) {
  http_response_code(404);
  header('Content-Type: text/plain; charset=utf-8');
  echo 'not found';
  exit;
}

function d4y_serve($file) {
  header('Content-Type: text/html; charset=UTF-8');
  // Платный контент не должен оседать в общих кэшах и на прокси.
  header('Cache-Control: private, no-store');
  readfile($file);
  exit;
}

$secret = d4y_secret();
if ($secret === null) { d4y_serve($file); }   // гейт спит — см. блок выше

$sess = d4y_verify(d4y_cookie(), $secret);

if ($sess && d4y_can_access($sess['role'], $page)) {
  d4y_serve($file);
}

// Доступа нет. Два разных случая — и это принципиально:
//
// 1. Куки нет вовсе. Скорее всего человек ЗАЛОГИНЕН, но пришёл по прямой
//    ссылке в новой вкладке: токен лежит в SDK, браузер его сам не пришлёт.
//    Отдаём страницу-загрузчик — она возьмёт токен, сходит в session.php,
//    поставит куку и перезагрузится. Снаружи это выглядит как привычный
//    спиннер, но КОНТЕНТ при этом не отправлен.
//
// 2. Кука есть, но роль не подходит — человек честно проверен и доступа у него
//    нет. Загрузчик не поможет, показываем пейволл сразу.
$mode = $sess ? 'denied' : 'check';

http_response_code($sess ? 403 : 200);
header('Content-Type: text/html; charset=UTF-8');
header('Cache-Control: private, no-store');

// Подстановкой, а не дописыванием в конец: скрипт внутри шаблона выполнится
// раньше, чем что-либо приписанное после него, и режима бы не увидел.
// $mode принимает ровно два значения выше — подставлять безопасно.
$shellFile = ($lang === 'en') ? 'gate-shell.en.html' : 'gate-shell.html';
$shell = file_get_contents(__DIR__ . '/../protected/' . $shellFile);
echo str_replace('{{MODE}}', $mode, $shell);
